'''9. Make a Program that asks for the temperature in degrees Fahrenheit, transforms and displays the temperature in degrees Celsius. C = 5 * ((F-32) / 9).'''

print("# Transform Celsius in Fahrenheit! #\n")

fahrenheit = float(input("Enter the degrees in Fahrenheit: "))

celsius = 5 * ((fahrenheit-32)/9)

print("The degrees in Celsius is: ", celsius)
